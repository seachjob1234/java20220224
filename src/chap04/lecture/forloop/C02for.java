package chap04.lecture.forloop;

public class C02for {
	public static void main(String[] args) {
		for (int i = 0; i < 5; i++) {
			System.out.println("hello");
		}
		// x값을 수숭해서 반복문이 7이 되도록
		int x = 7;
		for (int i = 1; i <= x; i++) {
			System.out.println("java");
		}
		for (int i = 5; i >= 0; i--) {
			System.out.println("spring");
		}
		int y = 14;
		for (int i = y; i >= 10; y--) {
			System.out.println("jsp");
		}


	}
}
