package chap06.lecture.p09getter_setter;

public class Desk {
	private String mogel;
	private int height;
	private int weight;
	private int price;
	
	
	public String getMogel() {
		return mogel;
	}
	public void setMogel(String mogel) {
		this.mogel = mogel;
	}
	public int getHeight() {
		return height;
	}
	public void setHeight(int height) {
		this.height = height;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
}
